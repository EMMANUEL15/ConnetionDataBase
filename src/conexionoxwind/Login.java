    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.jTextField1.getText();
        if(!this.jTextField1.getText().equals("") && !this.jPasswordField1.getText().equals("")){
            
            this.con = this.config.config();
            this.con.setPassword(this.jPasswordField1.getText());
            this.con.setUser(this.jTextField1.getText());
            if(this.con.conectar()){
                this.setVisible(false);
                new Controlventa(this.con.getConexion(),this.con.getHost()).setVisible(true);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese sus datos");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

